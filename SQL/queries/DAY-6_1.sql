SELECT CustomerID, SalesOrderID, 
OrderDate,
CAST(OrderDate AS DATE) AS OrderDate,
ROW_NUMBER() OVER(ORDER BY OrderDate) AS [ROW_NUMBER],
RANK() OVER(ORDER BY OrderDate) AS [RANK],
DENSE_RANK() OVER(ORDER BY OrderDate) AS [DENSE_RANK] 
FROM Sales.SalesOrderHeader
WHERE CustomerID = 11300;


SELECT CustomerID, SalesOrderID, CAST(OrderDate AS DATE) AS OrderDate,
ROW_NUMBER() OVER(PARTITION BY CUSTOMERID ORDER BY OrderDate) AS [ROW_NUMBER],
RANK() OVER(PARTITION BY CUSTOMERID ORDER BY OrderDate) AS [RANK],
DENSE_RANK() OVER(PARTITION BY CUSTOMERID ORDER BY OrderDate) AS [DENSE_RANK] 
FROM Sales.SalesOrderHeader
WHERE CustomerID = 11300
order by CustomerID


